def get_product_by_name(name):
    for p in PRODUCTS:
        if p["name"].lower() == name.lower():
            return p
    return None

def get_products_by_category(category):
    return [p for p in PRODUCTS if p["category"].lower() == category.lower()]

def get_high_priority_products():
    return [p for p in PRODUCTS if p["priority"] == "HIGH"]

def get_product_for_day(day_of_week):
    day_map = {
        0: "Monday", 1: "Tuesday", 2: "Wednesday", 3: "Thursday",
        4: "Friday", 5: "Saturday", 6: "Sunday"
    }
    target_day = day_map.get(day_of_week, "Monday")
    day_products = [p for p in PRODUCTS if p["best_day"] == target_day]
    if day_products:
        # Return first high priority if exists, else first available
        high_prio = [p for p in day_products if p["priority"] == "HIGH"]
        return high_prio[0] if high_prio else day_products[0]
    return PRODUCTS[0]
